//变量，数据类型
public class VariableSample {
	public static void main(String[] args) {		
		int x ;
		float y = 4.32F;
		long z = 23L;
		double a ;
		char YourSex ;
		x = 1;
		z = 5;
		a = 12.2;
		YourSex = 'M';
		System.out.println("x = " + x );
		System.out.println("y= " + y );
		System.out.println("z = " + z );
		System.out.println("a = " + a );
		System.out.println("YourSex = " + YourSex );
	}
}
