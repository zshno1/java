public class CompareOperationSample {
	public static void main(String[] args) {
		double x = 3.4;
		double y = 5.5;
		System.out.println("x > y ?    " + (x>y));
		System.out.println("x >= y ?    " + (x>=y));
		System.out.println("x < y ?    " + (x<y));
		System.out.println("x <= y ?    " + (x<=y));
		System.out.println("x == y ?    " + (x==y));
		System.out.println("x != y ?    " + (x!=y));
	}
}
