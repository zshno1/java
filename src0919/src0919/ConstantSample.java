public class ConstantSample {
	public static void main(String[] args) {
		final int X = 5;
		final double Y = 2.3;
		final long Z;
		final float Mark = 3.2F;
		Z = 100L;
		System.out.println("X = " + X);
		System.out.println("Y = " + Y);
		System.out.println("Z = " + Z);
		System.out.println("Mark = " + Mark);
	}
}
