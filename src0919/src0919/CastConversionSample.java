//数据类型
public class CastConversionSample {
	public static void main(String[] args) {
		int x = 50000;
		float y = 4.7F;
		double z = 5.55555445453;		
		System.out.println("(byte) x = " + (byte)x);
		System.out.println("(short) x = " + (short)x);
		System.out.println("(float) z = " + (float)z);
		System.out.println("(int) y = " + (int)y);
	}
}
